- 是否有固定办公地点
- 是否有公司网站和社交媒体

**红旗信号**：
- 无法提供营业执照
- 只通过WhatsApp联系
- 没有固定办公地点
- 网上找不到任何信息

**如何验证**：
1. 要求查看营业执照
2. 在行业协会网站查询
3. Google搜索公司名称
4. 查看Google评价和社交媒体

### 标准2：经验与专业度
