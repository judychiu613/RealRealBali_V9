## 四、合作流程

### 步骤1：初步接触
**行动**：
- 通过邮件或电话联系
- 说明需求和预算
- 要求提供房源列表

**评估**：
- 响应速度
- 沟通能力
- 专业度

### 步骤2：面谈
**行动**：
- 安排面对面会议（或视频会议）
- 详细讨论需求
- 了解中介背景和经验

**评估**：
- 专业形象
- 沟通能力
- 是否倾听需求

### 步骤3：签署委托协议
**内容**：
- 服务范围
- 佣金比例
- 服务期限
- 双方权利和义务

**注意**：
- 仔细阅读条款
- 明确退出机制
- 保留副本

### 步骤4：看房
**行动**：
- 中介安排看房
- 实地考察房产
- 了解周边环境

**评估**：
- 中介是否准时
- 是否了解房产细节
- 是否提供专业建议

### 步骤5：谈判与成交
**行动**：
- 中介协助谈判
- 协调律师、验房师
- 协助办理手续

**评估**：
- 谈判能力
- 协调能力
- 是否保护买家利益

### 步骤6：售后服务
**行动**：
- 协助办理过户
- 推荐物业管理公司
- 解答后续问题

**评估**：
- 是否负责到底
- 是否提供持续支持

