## 三、常见陷阱与如何避免

### 陷阱1：双重代理
**问题**：
- 中介同时代表买家和卖家
- 利益冲突
- 可能偏袒卖家（佣金更高）

**如何避免**：
- 明确询问中介是否代表卖家
- 如果是，考虑另找买方中介
- 或者要求降低佣金

### 陷阱2：虚假房源
**问题**：
- 房源不存在或已售出
- 用低价吸引客户
- 然后推荐其他高价房源

**如何避免**：
- 要求提供房产证书
- 实地看房前确认房源真实性
- 对过低的价格保持警惕

### 陷阱3：隐藏费用
**问题**：
- 不透明的佣金结构
- 额外的"服务费"
- 与第三方合作的回扣

**如何避免**：
- 要求书面费用清单
- 明确所有费用
- 对比多个中介的报价

### 陷阱4：夸大收益
**问题**：
- 夸大租金收入
- 夸大房产增值
- 隐瞒运营成本

**如何避免**：
- 要求提供数据支持
- 独立验证收益预测
- 咨询其他投资者

### 陷阱5：施压成交
**问题**：
- 制造紧迫感（"马上就要卖掉了"）
- 催促签约或付款
- 不给时间做尽职调查

**如何避免**：
- 保持冷静，不要被催促
- 坚持做完整的尽职调查
- 如果感觉不对，换中介

