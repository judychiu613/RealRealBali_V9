## 七、常见问题

### Q1：可以不用中介吗？
**A**：可以，但不推荐。中介的专业知识和资源网络很有价值，尤其是对首次投资者。

### Q2：可以同时找多个中介吗？
**A**：可以，但要告知每个中介。避免同一房源被多个中介推荐。

### Q3：如果对中介不满意怎么办？
**A**：如果没有签署独家协议，可以随时更换。如果签署了，查看合同中的退出条款。

### Q4：中介推荐的律师/验房师可靠吗？
**A**：通常可靠，但建议独立验证。可以要求推荐多个选项，自己选择。

### Q5：如何判断中介是否诚实？
**A**：交叉验证信息。咨询多个中介、查看公开数据、咨询其他投资者。

